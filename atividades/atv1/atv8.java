public class TESTE_01 {
    public static void main(String[] args) {
        // Declara a variável contador com valor inicial de 5
        int contador = 5;
        
        // Exibe o valor inicial
        System.out.println("Valor inicial do contador: " + contador);
        
        // Usa o operador de incremento para aumentar o valor
        contador++;
        System.out.println("Valor após incremento: " + contador);
        
        // Usa o operador de decremento para diminuir o valor
        contador--;
        System.out.println("Valor após decremento: " + contador);
    }
}